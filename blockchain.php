                        </p>
                    </section>

                    <section>
                        <h1>Présentation</h1>
                        <p>
    Technologie de stockage et de transmission d’informations, transparente, sécurisée, et fonctionnant sans organe central de contrôle.<br/>
    Base se de données qui contient l’historique de tous les échanges effectués entre ses utilisateurs depuis sa création<br/>
    Sécurisée et distribuée : elle est partagée par ses différents utilisateurs, sans intermédiaire, ce qui permet à chacun de vérifier la validité de la chaîne.<br/>
    Libre comptable public, anonyme, infalsifiable (aucune suppression)


                       </p>
                    </section>

                    <section>
                        <h1></h1>
                        <p>
                            Fonctionne avec une monnaie programmable : Bitcoin<br/>
                            Les transactions effectuées entre les utilisateurs du réseau sont regroupées par blocs. <br/>
                            Chaque bloc est validé par les noeuds du réseau appelés les “mineurs”, selon des techniques qui dépendent du type de blockchain. <br/>
                            Dans la blockchain du bitcoin cette technique est appelée le “Proof-of-Work”, preuve de travail, et constitue en la résolution de problèmes algorithmiques.<br/>
                            Une fois le bloc validé, il est horodaté et ajouté à la chaîne de blocs. <br/>
                            La transaction est alors visible pour le récepteur ainsi que l’ensemble du réseau.<br/>
                            Durée du processus : Bitcoin (15 min.), Ethereum (15 sec.)
                        </p>
                    </section>
                

                    <section>
                        <h1>Application</h1>
                            <p>              
                            Les applications pour le transfert d’actifs (utilisation monétaire, mais pas uniquement : titres, votes, actions, obligations…)
                            Les applications de la blockchain en tant que registre : elle assure ainsi une meilleure traçabilité des objets et des actifs.
                            Les smart contracts : il s’agit de programmes autonomes qui exécutent automatiquement les conditions et termes d’un contrat, sans nécessiter d’intervention humaine une fois démarrés.
                            </p>
                    </section>

                    <section>

                        <h1>Application</h1>
