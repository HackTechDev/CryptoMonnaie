                        <section>
                        <h1>Origine</h1>
                        <p>
                            décembre 2013, Vitalik Buterin
                             le 30 juillet 2015, permettant la création des premiers blocs de la blockchain  : bloc genesis 
                             l'émission de nouveaux ethers n'est possible que par le « minage » des blocs, processus par lequel les transactions sont vérifiées, enregistrées et sécurisées dans la « blockchain ». Le logiciel rétribue les mineurs à raison de 5 ethers par bloc miné, c'est-à-dire en moyenne toutes les 15 secondes, soit une émission de 10 millions de nouveaux ethers par an
                        </p>
                    </section>


                    <section>
                        <h1>Smart contract</h1>
                        <p>
                            Ces contrats intelligents sont basés sur un protocole informatique permettant de vérifier ou de mettre en application un contrat mutuel, ils sont déployés et consultables publiquement dans la blockchain.
                        </p>
